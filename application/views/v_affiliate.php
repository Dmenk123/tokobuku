  function login_proc() {
    $("#btnLogin").prop("disabled", true);
    var form = $('#form_login')[0];
    var data = new FormData(form);
    $.ajax({
        type: "POST",
        enctype: 'multipart/form-data',
        url: "<?php echo site_url('register/login'); ?>",
        data: data,
        dataType: "JSON",
        processData: false, // false, it prevent jQuery form transforming the data into a query string
        contentType: false, 
        cache: false,
        timeout: 600000,
        success: function (data) {
            if (data.status) {
                alert(data.pesan);
                $("#btnLogin").prop("disabled", false);
                window.location.href = "<?php echo site_url('profile'); ?>";
            }else{
              if (data.flag_alert) {
                alert(data.pesan);
              }else{
                $("#btnLogin").prop("disabled", false);
                for (var i = 0; i < data.inputerror.length; i++) {
                  if (data.inputerror[i] != 'jabatan') {
                    $('[name="' + data.inputerror[i] + '"]').parent().addClass('has-error');
                    $('[name="' + data.inputerror[i] + '"]').next().text(data.error_string[i]);
                  } else {
                    $($('#jabatan').data('select2').$container).addClass('has-error');
                  }
                }
              }              
            }
        },
        error: function (e) {
            console.log("ERROR : ", e);
            $("#btnRegister").prop("disabled", false);
        }
    });
  }
  
  function register_before() {
      window.location.href = "<?php echo site_url('affiliate/register'); ?>";
  }

</script>